#include <stdio.h>
#include <stdlib.h>
#include "Funciones.h"
int sumar(int a,int b)
{
    int resultado;
    resultado=a+b;
    return resultado;
}
int restar(int a,int b)
{
    int resultado;
    resultado=a-b;
    return resultado;
}
int multiplicar(int a,int b)
{
    int resultado;
    resultado=a*b;
    return resultado;
}
int dividir(int a,int b)
{
    int resultado;
    resultado=a/b;
    return resultado;
}
